#Multiple values to Multiple variables
x,y,z = "Swathi","shanthi","Sireesha"
print(x)
print(y)
print(z)

#One value to Multiple variables
x = y = z = "Swathi"
print(x)
print(y)
print(z)

#Unpack a collection
